package com.cloudbees.walmartqa1.exception;

public class ApiException extends Exception {

	private static final long serialVersionUID = 705653788331103543L;

	/**
	 * generated default
	 */
	public ApiException() {
	}

	/**
	 * generated message only
	 */
	public ApiException(String arg0) {
		super(arg0);
	}

	/**
	 * generated exception only
	 */
	public ApiException(Throwable arg0) {
		super(arg0);
	}

	/**
	 * generated message and exception only
	 */
	public ApiException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	/**
	 * generated full exception
	 */
	public ApiException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
